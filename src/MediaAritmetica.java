public class MediaAritmetica {
    public static void main(String[] args) {
        double nota1 = 8.5;
        double nota2 = 7.0;
        double nota3 = 9.2;

        double media = (nota1 + nota2 + nota3) / 3;

        System.out.println("Notas: " + nota1 + ", " + nota2 + ", " + nota3);
        System.out.println("Média aritmética: " + media);
    }
}
